	public String[] getRowAt(int row) {
		String[] resultados = new String[jTable1.getColumnCount()];

		for (int i = 0; i < jTable1.getColumnCount(); i++)
			resultados[i] = (String) jTable1.getModel().getValueAt(row, i);

		return resultados;
	}

